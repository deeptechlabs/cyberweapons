main:	clr	kTabPtr			;point into keyTable
	
encLoop:ldx	kTabPtr
	lda	keyTable,x
	bne	doTest			;is this list done?
done:	nop
	bra	main			;do it over again!
doTest:	inx				;bump past key length
	stx	kTabPtr			;copy over the test code
	; copy over the next example
	clr	tmpPtr
copyKey:ldx	kTabPtr			;copy key, split into even/odd
	lda	keyTable,x
	inx
	stx	kTabPtr
	ldx	tmpPtr
	sta	K32e,x
	inx
	stx	tmpPtr
	cpx	#KEY_SIZE/2		;time to skip SboxKey?
	bne	noSkipK
	txa
	add	#KEY_SIZE/4
	sta	tmpPtr

noSkipK:cpx	#KEY_SIZE+KEY_SIZE/4
	bne	copyKey
	clr	tmpPtr
copyBlk:ldx	kTabPtr
	lda	keyTable,x
	inx
	stx	kTabPtr
	ldx	tmpPtr
	sta	Text,x
	inx
	stx	tmpPtr
	cpx	#BLK_SIZE
	bne	copyBlk
	; compute remainder
	ldx	#K32e
	jsr	RSremainder
	ldx	#K32e+KBUMP
	jsr	RSremainder
	swap4	SboxKey,SboxKey+KBUMP

copied:	jsr	encryptBlock
	; should compare results here
	clr	tmpPtr
bp4:	nop
cmpLp:	ldx	kTabPtr			;compare cipherText
	lda	keyTable,X
	inx	
	stx	kTabPtr			;store bumped ptr
	ldx	tmpPtr
	cmp	Text,X
	bne	badEnc
	inx	
	stx	tmpPtr
	cpx	#BLK_SIZE
	bne	cmpLp
bp1:	nop				;compare ok!
	jsr	decryptBlock	

	lda	kTabPtr
	sub	#BLK_SIZE*2
	sta	kTabPtr			;back up
	clr	tmpPtr
cmpLp2:	ldx	kTabPtr			;compare cipherText
	lda	keyTable,X
	inx	
	stx	kTabPtr			;store bumped ptr
	ldx	tmpPtr
	cmp	Text,X
	bne	badDec
	inx	
	stx	tmpPtr
	cpx	#BLK_SIZE
	bne	cmpLp2
bp3:	nop
	lda	kTabPtr
	add	#BLK_SIZE
	sta	kTabPtr
	jmp	encLoop
bp2:	nop

badDec:	bra	$
badEnc:	bra	$
	
	.end	main
