;
;----------------------------------------------------------------
; MDS matrix:
;
;      01  EF  5B  5B		; 5B = alpha**-2 + 1
;      5B  EF  EF  01		; EF = alpha**-2 + alpha**-1 + 1
;      EF  5B  01  EF
;      EF  01  EF  5B
;----------------------------------------------------------------
;
; Load src byte, run through S-box.  Return result in X
;
codeSize	.var $
Sbox8ByteCnt	.var 0
Sbox8CallCnt	.var 0
