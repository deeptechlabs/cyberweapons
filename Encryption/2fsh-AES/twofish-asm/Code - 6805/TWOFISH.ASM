	.endm	

; key == 0 --> use X as ptr to key
f32	.macro	srcB0,rotCnt,step,dst,key
tmpf32		.var $
	;--- 1st byte, first MDS column = 01 5B EF EF
	Sbox8	srcB0+((rotCnt+step*0).AND.3),p0,p0,p1,key
  .if USE_MDS_TAB
	stx	tmp
  .else
    .if USE_ALPHA_TAB
	stx	dst		;X = 01
	ldx	alpha,X
	stx	tmp+1
	lda	alpha,X
    .else 
	sta	dst		;A = 01.  Store it
	mulAlpha		;a=a*alpha**-1
	sta	tmp+1
	mulAlpha
    .endif
	eor	dst		;now A = 5B
	sta	dst+1
	eor	tmp+1		;now A = EF
	sta	dst+2
	sta	dst+3
  .endif
	;--- 2nd byte, 2nd MDS column = EF EF 5B 01
	Sbox8	srcB0+((rotCnt+step*1).AND.3),p1,p0,p0,key+1
  .if USE_MDS_TAB
	stx	tmp+1
  .else
    .if USE_ALPHA_TAB
	stx	tmp
	txa
	eor	dst+3
	sta	dst+3
	ldx	alpha,X
	stx	tmp+1		;tmp+1 = alpha**-1
	lda	alpha,X
    .else
	sta	tmp		;save the 01 thing
	eor	dst+3
	sta	dst+3		;xor the 01
	lda	tmp
	mulAlpha
	sta	tmp+1
	mulAlpha
    .endif
	eor	tmp		;now A = 5B
	tax			;save 5B in X
	eor	dst+2
	sta	dst+2
	txa			;A = 5B
	eor	tmp+1		;now A = EF
	tax			;save 5F in X
	eor	dst+1
	sta	dst+1
	txa	
	eor	dst
	sta	dst
  .endif
	;--- 3rd byte, 3rd MDS column = 5B EF 01 EF
	Sbox8	srcB0+((rotCnt+step*2).AND.3),p0,p1,p1,key+2
  .if USE_MDS_TAB
	stx	dst+3		;very careful ordering
  .else
    .if USE_ALPHA_TAB
	stx	dst+2
	txa
	eor	dst+2
	sta	dst+2
	ldx	alpha,X
	stx	tmp+1		;tmp+1 = alpha**-1
	lda	alpha,X
    .else
	sta	tmp		;save the 01 thing
	eor	dst+2
	sta	dst+2		;xor the 01
	lda	tmp
	mulAlpha
	sta	tmp+1
	mulAlpha
    .endif
	eor	tmp		;now A = 5B
	tax			;save 5B in X
	eor	dst
	sta	dst
	txa			;A = 5B
	eor	tmp+1		;now A = EF
	tax			;save 5F in X
	eor	dst+1
	sta	dst+1
	txa	
	eor	dst+3
	sta	dst+3
  .endif
	;--- 4th byte, 4th MDS column = 5B 01 EF 5B
	Sbox8	srcB0+((rotCnt+step*3).AND.3),p1,p1,p0,key+3
  .if USE_MDS_TAB
	stx	dst+2
  .else
    .if USE_ALPHA_TAB
	stx	tmp
	txa
	eor	dst+1
	sta	dst+1
	ldx	alpha,X
	stx	tmp+1		;tmp+1 = alpha**-1
	lda	alpha,X
    .else
	sta	tmp		;save the 01 thing
	eor	dst+1
	sta	dst+1		;xor the 01
	lda	tmp
	mulAlpha
	sta	tmp+1
	mulAlpha
    .endif
	eor	tmp		;now A = 5B
	tax			;save 5B in X
	eor	dst
	sta	dst
	txa			;A = 5B
	eor	dst+3
	sta	dst+3
	txa
	eor	tmp+1		;now A = EF
	eor	dst+2
	sta	dst+2
  .endif
  .if USE_MDS_TAB		;S-box outputs=[tmp,tmp+2,dst+3,dst+2]
	lda	tmp		;here with X = 4th S-box output
	eor	tab5B,X
	ldx	tmp+1
	eor	tabEF,X
	ldx	dst+3
	eor	tab5B,X
	sta	dst		;X=3rd S-box output now

	lda	dst+2
	eor	tabEF,X
	ldx	tmp
	eor	tab5B,X
	ldx	tmp+1
	eor	tabEF,X
	sta	dst+1		;X=2nd S-box output now

	lda	dst+3
	eor	tab5B,X
	ldx	tmp
	eor	tabEF,X
	ldx	dst+2
	eor	tabEF,X
	sta	dst+2

	lda	tmp+1
	eor	tab5B,X
	ldx	tmp
	eor	tabEF,X
	ldx	dst+3
	eor	tabEF,X
	sta	dst+3
  .endif
